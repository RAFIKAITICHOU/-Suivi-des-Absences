    public void load(List<Absence> absences) {
        model.setRowCount(0);
        for (Absence a : absences) {
            model.addRow(new Object[]{
                a.getEtudiant().getNom() + " " + a.getEtudiant().getPrenom(),
                a.getSeance().getMatiere(),
                a.getSeance().getDate(),
                a.getJustification()
            });
        }
    }

