    private void btnRechercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRechercherActionPerformed
        // TODO add your handling code here:
         String nom = txtRecherche.getText().trim();
        if (nom.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Veuillez entrer un nom d'étudiant.", "Erreur", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            List<Absence> absences = absenceService.findByEtudiantNom(nom);
            if (absences.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Aucune absence trouvée pour cet étudiant.", "Information", JOptionPane.INFORMATION_MESSAGE);
                model.setRowCount(0);
            } else {
                load(absences);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erreur lors de la recherche : " + ex.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRechercherActionPerformed


